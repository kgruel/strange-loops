# Public glyph access
BIG_GLYPHS = _GLYPHS_3ROW  # Default export for backwards compatibility


def render_big(
    text: str,
    style: Style = Style(),
    *,
    size: int = 1,
    format: BigTextFormat = BigTextFormat.FILLED,
) -> Block:
    """Render text using block characters at configurable size.

    Args:
        text: String to render (case-folded to lowercase)
        style: Style applied to all block characters
        size: 1 for 3-row height (default), 2 for 5-row height
        format: FILLED (solid blocks) or OUTLINE (box-drawing characters)

    Returns:
        Block with height based on size, width based on text length
        Size 1: 3 cells per glyph + 1 gap = 4n - 1 width
        Size 2: 5 cells per glyph + 1 gap = 6n - 1 width
    """
    if not text:
        height = 3 if size == 1 else 5
        return Block.empty(0, height)

    # Normalize: lowercase, collapse whitespace
    text = text.lower()
    text = ''.join(' ' if c in '\t\n\r' else c for c in text)

    # Select glyph set based on size and format
    if size == 2:
        if format == BigTextFormat.OUTLINE:
            glyphs = _GLYPHS_5ROW_OUTLINE
            fallback = _GLYPHS_5ROW_OUTLINE['\x00']
        else:
            glyphs = _GLYPHS_5ROW
            fallback = _GLYPHS_5ROW['\x00']
        glyph_width = 5
    else:
        if format == BigTextFormat.OUTLINE:
            glyphs = _GLYPHS_3ROW_OUTLINE
            fallback = _GLYPHS_3ROW_OUTLINE['\x00']
        else:
            glyphs = _GLYPHS_3ROW
            fallback = _GLYPHS_3ROW['\x00']
        glyph_width = 3

    # Build glyph blocks
    glyph_blocks: list[Block] = []
    for char in text:
        glyph = glyphs.get(char, fallback)
        rows: list[list[Cell]] = []
        for row_str in glyph:
            row = [Cell(c, style) for c in row_str]
            rows.append(row)
        glyph_blocks.append(Block(rows, glyph_width))

    # Join with 1-cell gap
    return join_horizontal(*glyph_blocks, gap=1)
