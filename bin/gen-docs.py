#!/usr/bin/env python
"""Generate (and check) the source-derived documentation that would otherwise drift.

Run:
    uv run --package loops python bin/gen-docs.py            # regenerate in place
    uv run --package loops python bin/gen-docs.py --check    # verify; exit 1 if stale

Why this exists
---------------
Docs that *mirror* a machine-readable source of truth rot silently: a command gets
retired, a flag renamed, a symbol added to ``__all__`` — and the hand-written mirror
keeps lying until someone runs the thing. (Real cases this repo hit: ``loops export``
documented as live after retirement; ``--minimal`` documented when the flag is ``-q``;
``Grant(observer=...)`` documented after the field was removed.)

The rule — the dissolution test applied to docs:
    * A doc section is a GENERATION candidate iff it is a projection of a
      machine-readable source of truth (CLI argparse help, a package ``__all__``,
      pyproject deps, git log).
    * A doc section that expresses INTENT — the conceptual deep-dives, the guide
      ladder, design rationale, curated practice tables — stays hand-written.

See ``docs/generators.md`` for the full opportunity map (what is generated, what is a
candidate, what is deliberately not).

What this tool does
-------------------
GENERATE (no curation to lose — exhaustive flag detail):
    docs/cli-catalog.md   every verb/command, with status + verbatim ``--help``.
                          The command list is driven off ``loops.cli.registry`` so
                          a new/removed command appears/disappears automatically.

VALIDATE in --check (curation worth keeping — do not flatten):
    docs/api-reference.md Part 2 must mention every workspace lib's ``__all__``
                          symbol. Reports symbols newly exported-but-undocumented
                          and documented-but-no-longer-exported.

Determinism (so --check never false-positives):
    * COLUMNS is pinned to 80 — argparse wraps usage to terminal width otherwise.
    * Iteration follows registry dict order and ``__all__`` order (both stable);
      no timestamps in output; trailing whitespace stripped per line.
"""
from __future__ import annotations

import argparse
import difflib
import importlib
import io
import os
import sys
from contextlib import redirect_stdout
from pathlib import Path

# Pin terminal width BEFORE any argparse help is rendered — argparse honours
# COLUMNS, and unpinned width makes generated usage lines wrap nondeterministically.
os.environ["COLUMNS"] = "80"

ROOT = Path(__file__).resolve().parent.parent
DOCS = ROOT / "docs"
CLI_CATALOG = DOCS / "cli-catalog.md"
API_REFERENCE = DOCS / "api-reference.md"

# Workspace libraries whose public surface (``__all__``) Part 2 of the API
# reference documents. Order is the reference's own section order.
LIBS = ("atoms", "lang", "engine", "sign", "store")

# A tiny, explicit overlay for commands whose status can't be read from --help.
# ``export`` is still registered (registry.COMMANDS) but its body returns a
# Phase-3 retirement notice instead of running — see
# apps/loops/src/loops/commands/population.py (_run_export). Auto-probing for
# this by *executing* commands would be unsafe (side effects), so it's curated.
KNOWN_STATUS: dict[str, str] = {
    "export": "RETIRED (Phase 3) — still registered, but returns an error",
}

BANNER = (
    "<!-- GENERATED by bin/gen-docs.py — DO NOT EDIT BY HAND.\n"
    "     Regenerate: uv run --package loops python bin/gen-docs.py\n"
    "     Source of truth: loops.cli.registry + each command's argparse --help. -->"
)


def capture_help(name: str) -> str:
    """Return the verbatim ``loops <name> --help`` text, captured in-process.

    argparse prints help to stdout then raises SystemExit; we redirect, swallow
    the exit, and strip trailing whitespace per line for stable output.
    """
    from loops.cli.app import main

    buf = io.StringIO()
    try:
        with redirect_stdout(buf):
            main([name, "-h"])
    except SystemExit:
        pass
    lines = [line.rstrip() for line in buf.getvalue().splitlines()]
    return "\n".join(lines).strip()


def _render_command(name: str) -> str:
    status = KNOWN_STATUS.get(name)
    head = f"### `loops {name}`"
    if status:
        # Retired/stub commands: state the status; do not show --help (it doesn't
        # reach an argparser — the stub short-circuits).
        return f"{head}\n\n**{status}**\n"
    help_text = capture_help(name)
    return f"{head}\n\n```text\n{help_text}\n```\n"


def render_cli_catalog() -> str:
    """Build docs/cli-catalog.md from the registry + each command's --help."""
    from loops.cli import registry

    parts: list[str] = [
        BANNER,
        "",
        "# CLI Catalog — `loops` / `sl`",
        "",
        "Exhaustive, source-derived reference: every verb and command with its "
        "status and verbatim `--help`. Generated from `loops.cli.registry` and "
        "each command's argparse parser — it cannot drift from the installed "
        "surface.",
        "",
        "For the dispatch model, curated overview, and the Python library API, see "
        "[api-reference.md](api-reference.md). For worked examples and syntax "
        "depth, see [CLI-CHEATSHEET.md](CLI-CHEATSHEET.md).",
        "",
        "## Verbs (`registry.VERBS`)",
        "",
        "Primary vertex operations: `loops <verb> [vertex] [args]`.",
        "",
    ]
    for name in registry.VERBS:
        parts.append(_render_command(name))

    # Commands tier — skip names already shown as verbs (e.g. `store` is dual-registered).
    verb_names = set(registry.VERBS)
    parts += [
        "## Commands (`registry.COMMANDS`)",
        "",
        "Dev/setup actions that need no vertex resolution: `loops <command> [args]`.",
        "",
    ]
    for name in registry.COMMANDS:
        if name in verb_names:
            continue
        parts.append(_render_command(name))

    parts += [
        "## Vertex-first shorthand (`registry.VERTEX_OPS`)",
        "",
        "Dispatchable as `loops <vertex> <op>` (a bare `loops <vertex>` is implicit "
        "`read`):",
        "",
        # VERTEX_OPS is a frozenset — sort for deterministic output.
        "`" + "`, `".join(sorted(registry.VERTEX_OPS)) + "`",
        "",
    ]
    return "\n".join(parts).rstrip() + "\n"


def check_all_coverage() -> list[str]:
    """Verify api-reference.md Part 2 mentions every lib's ``__all__`` symbol.

    Returns a list of human-readable problems (empty == clean).
    """
    problems: list[str] = []
    text = API_REFERENCE.read_text(encoding="utf-8")
    for lib in LIBS:
        try:
            mod = importlib.import_module(lib)
        except Exception as exc:  # noqa: BLE001 — surface import failure as a problem
            problems.append(f"{lib}: could not import ({exc!r})")
            continue
        exported = list(getattr(mod, "__all__", []))
        if not exported:
            problems.append(f"{lib}: no __all__ to check")
            continue
        missing = [s for s in exported if s not in text]
        if missing:
            problems.append(
                f"{lib}: {len(missing)} __all__ symbol(s) not documented in "
                f"api-reference.md: {', '.join(missing)}"
            )
    return problems


def _diff(label: str, current: str, generated: str) -> list[str]:
    if current == generated:
        return []
    diff = difflib.unified_diff(
        current.splitlines(),
        generated.splitlines(),
        fromfile=f"{label} (on disk)",
        tofile=f"{label} (generated)",
        lineterm="",
    )
    return [f"{label} is STALE — regenerate with bin/gen-docs.py:", *list(diff)[:40]]


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=(__doc__ or "").splitlines()[0])
    parser.add_argument(
        "--check",
        action="store_true",
        help="Verify generated docs are current and api-reference covers __all__; "
        "exit 1 if not. (CI / pre-commit use.)",
    )
    args = parser.parse_args(argv)

    catalog = render_cli_catalog()

    if args.check:
        problems: list[str] = []
        on_disk = CLI_CATALOG.read_text(encoding="utf-8") if CLI_CATALOG.exists() else ""
        problems += _diff("docs/cli-catalog.md", on_disk, catalog)
        problems += check_all_coverage()
        if problems:
            print("✗ gen-docs --check found problems:\n", file=sys.stderr)
            for p in problems:
                print(p, file=sys.stderr)
            return 1
        print("✓ gen-docs --check: docs are current and __all__ is fully documented.")
        return 0

    CLI_CATALOG.write_text(catalog, encoding="utf-8")
    print(f"✓ wrote {CLI_CATALOG.relative_to(ROOT)}")
    coverage = check_all_coverage()
    if coverage:
        print("\n⚠ api-reference.md __all__ coverage gaps (hand-fix these):")
        for p in coverage:
            print(f"  - {p}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
